import postcss from 'postcss';
import postcssModules from 'postcss-modules';
import { readFileSync, writeFileSync, appendFileSync, mkdirSync } from 'fs';
import { fileURLToPath } from 'url';
import path from 'path';

const outFile = path.join(process.cwd(), 'styles', 'patterns.css');
let initialized = false;

export async function load(url, context, nextLoad) {
  if (!url.endsWith('.module.css')) {
    return nextLoad(url, context);
  }

  const filePath = fileURLToPath(url);
  const css = readFileSync(filePath, 'utf8');

  let classMap = {};
  const result = await postcss([
    postcssModules({
      generateScopedName(local, filename) {
        const name = path.basename(filename, '.module.css');
        return `${name}__${local}`;
      },
      getJSON(_, json) {
        classMap = json;
      },
    }),
  ]).process(css, { from: filePath });

  if (!initialized) {
    mkdirSync(path.dirname(outFile), { recursive: true });
    writeFileSync(outFile, '/* Generated by build-patterns — do not edit */\n\n');
    initialized = true;
  }
  appendFileSync(outFile, result.css + '\n');

  return {
    format: 'module',
    shortCircuit: true,
    source: `export default ${JSON.stringify(classMap)};`,
  };
}
